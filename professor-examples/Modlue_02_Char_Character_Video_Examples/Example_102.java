public class Example_102{

  public static void main(String [] args){

    char letter_01 = '\u0041';
    char letter_02 = '\u0062';
    char letter_03 = '\u007A';

    char numChar_01 = '\u0031';
    char numChar_02 = '\u0033';
    char numChar_03 = '\u0037';
 
    System.out.println("\t" + letter_01);
    System.out.println("\t" + letter_02);
    System.out.println("\t" + letter_03);

    System.out.println("\t" + numChar_01);
    System.out.println("\t" + numChar_02);
    System.out.println("\t" + numChar_03);

  }
}