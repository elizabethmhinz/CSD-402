/*
 * Escape Sequence
 */
public class Example_017{

  public static void main(String [] args){

    System.out.println("\tWelcome\t\tTo\tJava.");
    System.out.println();
    System.out.println("\nWelcome\nTo\nJava.");
    System.out.println();
    System.out.println("\bWelcome\bTo\bJava.");
    System.out.println();
    System.out.println("\\Welcome\\To\\Java.");
    System.out.println();
    System.out.println("\"Welcome To Java.\"");
  }
}