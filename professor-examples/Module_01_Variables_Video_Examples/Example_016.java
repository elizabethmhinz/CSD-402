/*
 * Casting (Type Conversions)
 */
public class Example_016{

  public static void main(String [] args){

    double var_01 = 23.43;
    double var_02 = 2.14;

    System.out.println(var_01 / var_02);
    System.out.println((int)(var_01 / var_02));

    System.out.println(var_01 * var_02);
    System.out.println((int)(var_01 * var_02));
  }
}